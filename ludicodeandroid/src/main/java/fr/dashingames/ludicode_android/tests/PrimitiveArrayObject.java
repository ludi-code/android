package fr.dashingames.ludicode_android.tests;

public class PrimitiveArrayObject {

	private double pie;
	private int[] array;
	
	public PrimitiveArrayObject(double pie, int[] array) {
		super();
		this.pie = pie;
		this.array = array;
	}

	public double getPie() {
		return pie;
	}

	public void setPie(double pie) {
		this.pie = pie;
	}

	public int[] getArray() {
		return array;
	}

	public void setArray(int[] array) {
		this.array = array;
	}	
}
